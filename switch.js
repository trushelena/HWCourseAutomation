let experience = 5;

switch (experience) {
    case (experience = 1):
        console.log("You are Junior");
        break;
    case (experience = 3):
        console.log("You are Middle");
        break;
    case (experience = 5):
        console.log("You are Middle");
        break;
    default:
        console.log("You are waiting for your offer");
}
