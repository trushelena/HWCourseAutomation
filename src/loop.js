// for loop - show up to 9
for (let i = 0; i < 10; i++) {
    console.log(i); // Show value for each iteration
};

// while loop - show up to 9
let i = 0;
while (i < 10) { // how value for each iteration
    console.log(i);
    i++;
}

// for loop - show from 100 to 0
for (let i = 100; i >=0; i-=10) {
    console.log(i); // Show value for each iteration
};

// while loop - show from 100 to 0
let k = 100;
while (k >= 0) { // how value for each iteration
    console.log(k);
    k-=10;
};
